interface IconProps {
  name: string
  className?: string
  strokeWidth?: number
}

export function Icon({ name, className, strokeWidth = DEFAULT_STROKE }: IconProps) {
  const sized = className ?? 'w-5 h-5'
  const Brand = brandMap[name]
  if (Brand) return <Brand className={sized} />
  const Lucide = lucideMap[name]
  if (!Lucide) return null
  return <Lucide className={cn('shrink-0', sized)} strokeWidth={strokeWidth} aria-hidden="true" />
}

export function hasIcon(name: string): boolean {
  return name in brandMap || name in lucideMap
}

export function isBrandIcon(name: string): boolean {
  return name in brandMap
}
